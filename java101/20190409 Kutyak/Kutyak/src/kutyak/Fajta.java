public class Fajta {

    private String fajta, eredeti;

    public Fajta(String fajta, String eredeti) {
        this.fajta = fajta;
        this.eredeti = eredeti;
    }

    public String getFajta() {
        return fajta;
    }

    public String getEredeti() {
        return eredeti;
    }
}
